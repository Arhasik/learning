fun main() {
    // Запрашиваем у пользователя целое положительное число n
    print("Введите целое положительное число: ")
    val n = readLine()?.toIntOrNull()

    // Проверяем, что пользователь ввел корректное число
    if (n != null && n > 1) {
        var isPrime = true
        var i = 2

        // Используем цикл while для проверки, является ли число простым
        while (i <= n / 2) {
            if (n % i == 0) {
                isPrime = false
                break
            }
            i++
        }

        // Выводим результат проверки
        if (isPrime) {
            println("Число простое")
        } else {
            println("Число не является простым")
        }
    } else {
        // Если введено некорректное значение
        println("Ошибка: введите целое положительное число больше 1.")
    }
}